
def rotate180(dots):
    dots2 = [0b00000000,0b00000000,0b00000000,0b00000000,0b00000000,0b00000000] #磁点

    dots2[3] |= (dots[0] & 0b00000001) << 5    # 1->30
    dots2[3] |= (dots[0] & 0b00000010) << 3    # 2->29
    dots2[3] |= (dots[0] & 0b00000100) << 1    # 3->28
    dots2[3] |= (dots[0] & 0b00001000) >> 1    # 4->27
    dots2[3] |= (dots[0] & 0b00010000) >> 3    # 5->26
    dots2[3] |= (dots[0] & 0b00100000) >> 5    # 6->25
    dots2[2] |= (dots[0] & 0b01000000) << 1    # 7->24
    dots2[2] |= (dots[0] & 0b10000000) >> 1    # 8->23
    dots2[2] |= (dots[1] & 0b00000001) << 5    # 9->22
    dots2[2] |= (dots[1] & 0b00000010) << 3    # 10->21
    dots2[2] |= (dots[1] & 0b00000100) << 1    # 11->20
    dots2[2] |= (dots[1] & 0b00001000) >> 1    # 12->19
    dots2[2] |= (dots[1] & 0b00010000) >> 3    # 13->18
    dots2[2] |= (dots[1] & 0b00100000) >> 5    # 14->17
    dots2[1] |= (dots[1] & 0b01000000) << 1    # 15->16

    dots2[1] |= (dots[1] & 0b10000000) >> 1    # 16->15
    dots2[1] |= (dots[2] & 0b00000001) << 5    # 17->14
    dots2[1] |= (dots[2] & 0b00000010) << 3    # 18->13
    dots2[1] |= (dots[2] & 0b00000100) << 1    # 19->12
    dots2[1] |= (dots[2] & 0b00001000) >> 1    # 20->11
    dots2[1] |= (dots[2] & 0b00010000) >> 3    # 21->10
    dots2[1] |= (dots[2] & 0b00100000) >> 5    # 22->9
    dots2[0] |= (dots[2] & 0b01000000) << 1    # 23->8
    dots2[0] |= (dots[2] & 0b10000000) >> 1    # 24->7
    dots2[0] |= (dots[3] & 0b00000001) << 5    # 25->6
    dots2[0] |= (dots[3] & 0b00000010) << 3    # 26->5
    dots2[0] |= (dots[3] & 0b00000100) << 1    # 27->4
    dots2[0] |= (dots[3] & 0b00001000) >> 1    # 28->3
    dots2[0] |= (dots[3] & 0b00010000) >> 3    # 29->2
    dots2[0] |= (dots[3] & 0b00100000) >> 5    # 30->1

    dots2[5] |= (dots[3] & 0b01000000) >> 3    # 31->44
    dots2[5] |= (dots[3] & 0b10000000) >> 5    # 32->43
    dots2[5] |= (dots[4] & 0b00000001) << 1    # 33->42
    dots2[5] |= (dots[4] & 0b00000010) >> 1    # 34->41
    dots2[4] |= (dots[4] & 0b00000100) << 5    # 35->40
    dots2[4] |= (dots[4] & 0b00001000) << 3    # 36->39
    dots2[4] |= (dots[4] & 0b00010000) << 1    # 37->38
    dots2[4] |= (dots[4] & 0b00100000) >> 1    # 38->37
    dots2[4] |= (dots[4] & 0b01000000) >> 3    # 39->36
    dots2[4] |= (dots[4] & 0b10000000) >> 5    # 40->35
    dots2[4] |= (dots[5] & 0b00000001) << 1    # 41->34
    dots2[4] |= (dots[5] & 0b00000010) >> 1    # 42->33
    dots2[3] |= (dots[5] & 0b00000100) << 5    # 43->32
    dots2[3] |= (dots[5] & 0b00001000) << 3    # 44->31
    return dots2

dots = [0b11111111,0b11111111,0b11111111,0b11111111,0b11111111,0b11111111] #磁点

print(['b{:08b}'.format(dot) for dot in dots])
dots2 = rotate180(dots) #反转180度
print(['b{:08b}'.format(dot) for dot in dots2])